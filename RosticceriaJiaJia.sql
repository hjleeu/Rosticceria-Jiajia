CREATE TABLE Sezioni (
	id_sezione		INT PRIMARY KEY NOT NULL,
	nome_sezione	VARCHAR(50) NOT NULL,
);

CREATE TABLE Prodotti (
	id_prodotto		INT PRIMARY KEY IDENTITY(1, 1),    -- Id che non corrisponde a quello del menu.
	prt_nome        VARCHAR(10),                                -- Vero id, che verà stampato.
    nome_prodotto	VARCHAR(50) NOT NULL,
	prezzo_prodotto	FLOAT NOT NULL,
    acc_ordine      INT NOT NULL DEFAULT 1,                     -- 1 Si può ordinare, 0 non si può.
    acc_menu        INT NOT NULL,                               -- 1 Accettabile secondo per Menu bis o tris, 0 no.
    id_sezione		INT NOT NULL,
	FOREIGN KEY(id_sezione) REFERENCES Sezioni(id_sezione),
);

CREATE TABLE Ordinazioni (
    id_ordinazione      INT PRIMARY KEY NOT NULL IDENTITY(1, 1),
    nome_cliente        VARCHAR(50),
    data_ordinazione    TIME(0) NOT NULL,
    totale_ordinazione  FLOAT NOT NULL DEFAULT 0,
    posate              INT NOT NULL DEFAULT 0,   -- 0 No, 1 Si. 
);

CREATE TABLE SpecificheOrdinazioni (
    id_ordinazione  INT NOT NULL,
    id_prodotto     INT NOT NULL,
    quantita        INT NOT NULL,
    note            VARCHAR(50),
    FOREIGN KEY (id_ordinazione) REFERENCES Ordinazioni(id_ordinazione),
    FOREIGN KEY (id_prodotto) REFERENCES Prodotti(id_prodotto),
)

ALTER TABLE Prodotti
ALTER COLUMN prt_nome VARCHAR(10) COLLATE Chinese_PRC_Stroke_CI_AS;

ALTER TABLE SpecificheOrdinazioni
ALTER COLUMN note VARCHAR(50) COLLATE Chinese_PRC_Stroke_CI_AS;

